            <h2>
                Comer atentamente
            </h2>
            <p>
                A alimentação consciente é uma prática em que as pessoas prestam atenção em como e onde comem alimentos. Essa prática pode permitir que as pessoas desfrutem da comida que ingerem e mantenham um peso saudável.<br>
                Como a maioria das pessoas leva uma vida ocupada, muitas vezes tendem a comer rapidamente em fuga, no carro, trabalhando em suas mesas e assistindo TV. Como resultado, muitas pessoas mal sabem da comida que estão comendo.<br><br>

                As técnicas para uma alimentação consciente incluem:<br><br>

                Sentar-se para comer, de preferência à mesa: preste atenção à comida e aproveite a experiência.<br><br>
                Evitar distrações enquanto come: Não ligue a TV, o laptop ou o telefone.<br><br>
                Comer devagar: reserve um tempo para mastigar e saborear a comida. Essa técnica ajuda na perda de peso, pois dá ao cérebro de uma pessoa tempo suficiente para reconhecer os sinais de que está cheia, o que pode ajudar a evitar o excesso de alimentação.<br><br>
                Fazendo escolhas alimentares consideradas: Escolha alimentos cheios de nutrientes nutritivos e aqueles que irão satisfazer por horas, em vez de minutos.<br><br>
