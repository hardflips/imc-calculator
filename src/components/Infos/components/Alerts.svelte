            <h2>
                Tentando jejum intermitente
            </h2>
            <p>
                O jejum intermitente (IF) é um padrão de alimentação que envolve jejuns regulares de curto prazo e o consumo de refeições em um período mais curto durante o dia. <br>
                Vários estudos indicaram que o jejum intermitente a curto prazo, com duração de até 24 semanas, leva à perda de peso em indivíduos com sobrepeso.<br><br>

                Os métodos mais comuns de jejum intermitente incluem o seguinte:<br><br>

                Jejum alternativo (ADF): Jejue em dias alternados e coma normalmente em dias sem jejum.<br><br>
                A versão modificada envolve a ingestão de apenas 25 a 30% das necessidades de energia do corpo em dias de jejum.<br><br>
                A dieta 5: 2: jejue em 2 de cada 7 dias. Nos dias de jejum consome de 500 a 600 calorias.<br><br>
                O método 16/8: jejue por 16 horas e coma apenas durante uma janela de 8 horas. Para a maioria das pessoas, a janela de 8 horas seria em torno do meio-dia às 20h. Um estudo sobre esse método constatou que comer durante um período restrito resultava nos participantes consumindo menos calorias e perdendo peso.<br><br>
                É melhor adotar um padrão de alimentação saudável em dias sem jejum e evitar comer demais.<br><br>
            </p>
            <h2>
                Acompanhando sua dieta e exercício
            </h2>
            <p>
                Se alguém quer perder peso, deve estar ciente de tudo o que come e bebe todos os dias. A maneira mais eficaz de fazer isso é registrar todos os itens que eles consomem, em um diário ou em um rastreador de comida online. <br> <br>
                Os pesquisadores estimaram em 2017 que haveria 3,7 bilhões de downloads de aplicativos de saúde até o final do ano. Desses, aplicativos para dieta, atividade física e perda de peso estavam entre os mais populares. Isso não é sem razão, pois o rastreamento da atividade física e o progresso da perda de peso em movimento pode ser uma maneira eficaz de gerenciar o peso. <br> <br>
                Um estudo descobriu que o rastreamento consistente da atividade física ajudou na perda de peso. Enquanto isso, um estudo de revisão encontrou uma correlação positiva entre a perda de peso e a frequência de monitoramento da ingestão de alimentos e exercícios. Mesmo um dispositivo tão simples quanto um pedômetro pode ser uma ferramenta útil para perda de peso.<br><br>
