            <p>
                Para a maioria dos adultos, o IMC fornece uma boa estimativa dos riscos à saúde relacionados ao peso. Se o seu IMC for superior a 35, seu peso está definitivamente colocando sua saúde em risco, independentemente dos fatores abaixo. No entanto, existem algumas situações em que o IMC pode subestimar ou superestimar esses riscos na faixa de 25-35 IMC. Os principais são:<br><br>
