            <h2>
                Dê uma olhada no seu estilo de vida
            </h2>
            <p>
                As probabilidades são de que você pode fazer mudanças no estilo de vida para melhorar seu número de colesterol. Isso é verdade porque, na maioria das vezes, comportamentos não saudáveis ​​são os principais culpados por trás do colesterol alto.<br>
                Seu corpo produz naturalmente todo o colesterol LDL (ruim) necessário. Um estilo de vida pouco saudável - pouco exercício, muitos alimentos pouco saudáveis ​​- faz com que seu corpo produza mais colesterol LDL do que precisa. Esta é a causa do colesterol LDL alto para a maioria das pessoas.<br><br>
                Comportamentos que podem afetar negativamente seus níveis de colesterol incluem:<br><br>
                Dieta não saudável<br>
                Falta de atividade física<br>
                Tabagismo ou exposição ao fumo do tabaco<br>
                Excesso de peso<br><br>
                Interromper ou reverter esses fatores prejudiciais ao estilo de vida pode ajudar a melhorar seus números de colesterol.<br><br>
