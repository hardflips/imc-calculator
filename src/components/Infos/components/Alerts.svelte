                Relação cintura / altura
            </h2>
            <p>
                Um estudo recente comparando o IMC, a circunferência da cintura, a relação cintura / quadril e a relação cintura / altura descobriu que a maneira mais precisa de prever seu nível de gordura corporal total era a relação cintura-altura.<br><br>
                Meça a circunferência da cintura como acima e simplesmente divida-a pela sua altura - obviamente, ambas as medidas devem ser imperiais (polegadas) ou métricas (cm).<br><br>
                A obesidade de corpo inteiro é definida por uma relação cintura-altura de:<br><br>
                <strong>0,53</strong> ou mais para homens<br>
                <strong>0,54</strong> ou mais para mulheres<br><br>
                A obesidade abdominal foi definida por uma relação cintura-altura de 0,59 ou mais.
