            <h2>
                Se você é muito musculoso
            </h2>
            <p>
            O IMC supõe que você tenha uma quantidade média de gordura corporal, incluindo 'gordura intra-abdominal' - gordura profundamente dentro da cavidade do estômago, em vez de sob a pele. A gordura intra-abdominal está muito mais intimamente ligada aos riscos de diabetes tipo 2 e doenças cardíacas do que a gordura sob a pele.
            Se você é muito musculoso, seu nível de gordura corporal pode ser menor do que o previsto pelo seu IMC. No entanto, isso se aplica apenas a pessoas que praticam altos níveis de exercício - muito mais que a média.<br><br>
            </p>
            <h2>
                De origem asiática
            </h2>
            <p>
                Pessoas de origem asiática tendem a acumular gordura intra-abdominal (gordura profunda dentro da cavidade do estômago, em vez de sob a pele), com um IMC menor do que as pessoas de origem caucasiana. Pessoas com esse padrão de ganho de peso são descritas como "maçãs" em vez de "peras" a partir do contorno corporal. Isso significa que seus riscos à saúde começam a aumentar com um IMC menor, porque a gordura intra-abdominal está diretamente ligada ao desenvolvimento de doenças cardíacas e diabetes tipo 2.
                A Organização Mundial da Saúde analisou as evidências e, devido à variabilidade entre diferentes populações asiáticas, não mudou oficialmente os pontos de corte. No entanto, recomenda que, para fins de saúde pública, alguns grupos asiáticos sejam considerados com sobrepeso se o IMC for 22-25 e obesos com um IMC de 26-31.<br><br>
            </p>
